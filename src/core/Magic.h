struct MagicEntry {
  Bitboard Mask;
  uint64_t Magic;
  int Shift;

  constexpr MagicEntry(Bitboard Mask, uint64_t Magic, int Shift)
      : Mask(Mask), Magic(Magic), Shift(Shift) {}

  int magicIndex(Bitboard Occ) const {
    Occ &= Mask;
    Occ *= Magic;
    Occ >>= Shift;

    return Occ;
  }
};

